package com.example.finalprojectmobile2.model;

public class UserModel {
    String username;

    // Constructors
    public UserModel(String username) {
        this.username = username;
    }
    public UserModel() {
    }


    // Getters and Setters
    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }
}
